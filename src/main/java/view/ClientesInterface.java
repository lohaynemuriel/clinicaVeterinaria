    private void configurandoTabelaPrincipal() {
    // 1. Consultar o banco de dados para obter os valores dos animais
    ClienteDAO clienteDAO = ClienteDAO.getInstance();
    List<Cliente> clientes = clienteDAO.retrieveAll();
    
    // 2. Criar um modelo de tabela personalizado
    DefaultTableModel tableModel = new DefaultTableModel();
    tableModel.addColumn("Nome");
    tableModel.addColumn("Endere√ßo");
    tableModel.addColumn("CEP");
    tableModel.addColumn("E-mail");
    tableModel.addColumn("Telefone");

    // 3. Preencher a tabela com os valores obtidos
    for (Cliente cliente : clientes) {
        tableModel.addRow(new Object[]{
            cliente.getNome(),
            cliente.getEndereco(),
            cliente.getCEP(),
            cliente.getEmail(),
            cliente.getTelefone()
        });
        }
    }
